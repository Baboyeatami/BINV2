    private void SaveIntelReport(int informant_id, String barangay, String subject, String IntelReport) throws ParseException {

        try {
            DBConnection.init();
            Connection c = DBConnection.getConnection();
            PreparedStatement ps;

            String timeStamp = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss").format(Calendar.getInstance().getTime());

            SimpleDateFormat df = new SimpleDateFormat("yyyy/MM/d");
            String dateString = String.valueOf(df.format(date.getDate()));
            Date myDate = df.parse(dateString);
            System.out.println(dateString);
            ps = c.prepareStatement("Insert Into intel_reports ( informantid, Barangay, subject, date, Intel_report, DateCreated, DateUpdated) Values ('" + informant_id + "','" + barangay + "','" + subject + "','" + dateString + "','" + IntelReport + "','" + timeStamp + "','" + timeStamp + "')");
            Boolean Status = ps.execute();

            if (!Status) {
                JOptionPane.showMessageDialog(this, "Intel Report Added", "New Intel", JOptionPane.INFORMATION_MESSAGE);
                int a = JOptionPane.showConfirmDialog(this, "Do you want to add new Intel Report to this Informant? ", "New Intel Report", JOptionPane.YES_NO_OPTION);
                if (a == 0) {
                    this.IntelReport.setText("");
                    date.setDate(null);
                    subject_combo.setSelectedIndex(0);
                } else {
                    this.dispose();
                }

            }
        } catch (SQLException ex) {
            Logger.getLogger(IntelReport.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

